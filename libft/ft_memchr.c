void   *ft_memchr(const void *s, int c, size_t t)
{
    const char  *str;
    size_t  i;

    str = (const char *)s;
    i = 0;

    while (i < t)
    {
        if (str[i] == c)
            return ((void *)(s + i));
        i++;
    }
    return (0);
}